async function main() {
    // 从 .env 加载密钥
    const envPath = path.join(__dirname, '..', '.env');
    let serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
    if (!serviceKey) {
        const envContent = fs.readFileSync(envPath, 'utf-8');
        const lines = envContent.split('\n');
        for (const line of lines) {
            const t = line.trim();
            if (t.startsWith('SUPABASE_SERVICE_ROLE_KEY=')) {
                serviceKey = t.split('=').slice(1).join('=').trim();
                break;
            }
        }
    }

    const BASE = 'https://orphftlwdwuvoscizndx.supabase.co/rest/v1';
    const headers = {
        'apikey': serviceKey,
        'Authorization': `Bearer ${serviceKey}`,
        'Content-Type': 'application/json',
        'Prefer': 'return=minimal',
    };

    // 1. 拉取 BV1vm 的歌曲 (IDs 311-510)
    console.log('获取 BV1vm411Z7ZN 的歌曲...');
    const songsResp = await fetch(
        `${BASE}/songs?select=id,title,singer&bvid=eq.BV1vm411Z7ZN&order=id`,
        { headers }
    );
    const songs = await songsResp.json();
    console.log(`找到 ${songs.length} 首歌曲\n`);

    // 2. 计算每首歌的标签
    let insertCount = 0;
    let skipCount = 0;
    let noTagCount = 0;

    for (const song of songs) {
        const singer = song.singer || '';
        const title = song.title || '';
        const tagSet = new Set();

        // 按歌手匹配（支持精确匹配和包含匹配）
        for (const [singerName, tags] of Object.entries(SINGER_TAGS)) {
            if (singer === singerName ||
                singer.includes(singerName) ||
                singerName.includes(singer)) {
                tags.forEach(t => tagSet.add(t));
            }
        }

        // 按歌名关键词匹配
        for (const { kw, tags } of TITLE_KEYWORDS) {
            if (title.includes(kw)) {
                tags.forEach(t => tagSet.add(t));
            }
        }

        // 默认标签
        if (tagSet.size === 0) {
            tagSet.add('热门');
            noTagCount++;
            if (noTagCount <= 10) {
                console.log(`  ⚠ 无匹配标签: ${title} - ${singer} → 默认"热门"`);
            }
        }

        // 3. 插入 song_tags
        for (const tagName of tagSet) {
            const tagId = TAG_IDS[tagName];
            if (!tagId) {
                // 尝试匹配父标签
                if (tagName === '日语') {
                    // 日语不是独立标签，跳过
                    continue;
                }
                console.log(`  ⚠ 未知标签名: ${tagName} (歌曲: ${title})`);
                continue;
            }

            try {
                const resp = await fetch(`${BASE}/song_tags`, {
                    method: 'POST',
                    headers,
                    body: JSON.stringify({ song_id: song.id, tag_id: tagId }),
                });
                if (resp.ok) {
                    insertCount++;
                } else if (resp.status === 409) {
                    skipCount++;
                } else {
                    const err = await resp.text();
                    if (insertCount < 20) {
                        console.log(`  ✗ ${title} -> ${tagName}: ${err.slice(0, 80)}`);
                    }
                }
            } catch (err) {
                console.log(`  ✗ ${title} -> ${tagName}: ${err.message}`);
            }
        }
    }

    console.log(`\n========== 完成 ==========`);
    console.log(`标签插入: ${insertCount} 条`);
    console.log(`重复跳过: ${skipCount} 条`);
    console.log(`无匹配歌曲: ${noTagCount} 首`);
    console.log(`歌曲总数: ${songs.length} 首`);
}

main().catch(e => {
    console.error('致命错误:', e);
    process.exit(1);
});
